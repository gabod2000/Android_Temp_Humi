package io.terasyshub.models;

public class Device {
    String id,mac,name;

    public Device() {}

    public Device(String id, String mac, String name) {
        this.id = id;
        this.mac = mac;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMac() {
        return mac;
    }

    public void setMac(String mac) {
        this.mac = mac;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


    @Override
    public String toString() {
        return name;
    }
}
